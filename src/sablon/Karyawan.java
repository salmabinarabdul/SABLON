    void Tambah() {
        try {
            Connection con = Koneksi.bukaKoneksi();
            Statement st = con.createStatement();
            //tempat data ditambahkan
            String sql = "insert into pemesanan values('" + kodeTxt.getText() + "'"
                    + ",'" + namaTxt.getText() + "'"
                    + ",'" + telpTxt.getText() + "'"
                    + ",'" + alamatTxt.getText() + "'"
                    + ",'" + kainTxt.getSelectedItem().toString() + "'"
                    + ",'" + warnaTxt.getText() + "'"
                    + ",'" + sablonTxt.getSelectedItem().toString() + "'"
                    + ",'" + jumlahTxt.getValue().toString() + "'"
                    + ",'" + sizeTxt.getSelectedItem().toString() + "'"
                    + ",'" + genderTxt.getSelectedItem().toString() + "'"
                    + ",'" + hargaTxt.getValue().toString() + "')";

            int row = st.executeUpdate(sql);

            //pesan jika data sukses ditambahkan
            if (row == 1) {
                JOptionPane.showMessageDialog(null, "Data sudah ditambahkan ke" + "database", "informasi", JOptionPane.INFORMATION_MESSAGE);
                EmptyForm();
                con.close();
            }
        } //data gagal dimasukkan pada database
        catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data gagal ditambahkan ke" + "database\n" + e.getMessage(), "informasi", JOptionPane.ERROR_MESSAGE);
        }
    }

