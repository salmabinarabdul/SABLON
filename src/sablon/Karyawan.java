    private void load_table_pesanan() {
        // membuat tampilan model tabel
        DefaultTableModel model = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int column){
                return false;
            }
        };
        model.addColumn("ID");
        model.addColumn("Nama pemesan");
        model.addColumn("Telp");
        model.addColumn("Alamat");
        model.addColumn("Jenis kain");
        model.addColumn("Warna");
        model.addColumn("Jenis sablon");
        model.addColumn("Jumlah");
        model.addColumn("Size");
        model.addColumn("Jenis kelamin");
        model.addColumn("Harga");
        //menampilkan data database kedalam tabel
        try {
            String sql;
            if (!"".equals(searchBox.getText())) {
                sql = "select * from pemesanan where id_pesanan like '%" + searchBox.getText() + "%'"
                        + "or nama_pemesan like '%" + searchBox.getText() + "%' "
                        + "or no_hp like '%" + searchBox.getText() + "%' "
                        + "or alamat like '%" + searchBox.getText() + "%' "
                        + "or jenis_kain like '%" + searchBox.getText() + "%' "
                        + "or warna like '%" + searchBox.getText() + "%' "
                        + "or jenis_sablon like '%" + searchBox.getText() + "%' "
                        + "or jumlah like '%" + searchBox.getText() + "%' "
                        + "or size like '%" + searchBox.getText() + "%' "
                        + "or jenis_kelamin like '%" + searchBox.getText() + "%' "
                        + "or harga like '%" + searchBox.getText() + "%'";
            } else {
                sql = "select * from pemesanan";
            }
            java.sql.Connection conn = (Connection) Koneksi.bukaKoneksi();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            while (res.next()) {
                model.addRow(new Object[]{
                    res.getString(1),
                    res.getString(2),
                    res.getString(3),
                    res.getString(4),
                    res.getString(5),
                    res.getString(6),
                    res.getString(7),
                    res.getString(8),
                    res.getString(9),
                    res.getString(10),
                    res.getString(11)
                });
            }
            tblPesanan.setModel(model);

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", 0);
        }
    }
    
    private void select_data(){
        int baris = tblPesanan.getSelectedRow();
        kodeTxt.setText(tblPesanan.getValueAt(baris, 0).toString());
        namaTxt.setText(tblPesanan.getValueAt(baris, 1).toString());
        telpTxt.setText(tblPesanan.getValueAt(baris, 2).toString());
        alamatTxt.setText(tblPesanan.getValueAt(baris, 3).toString());
        kainTxt.setSelectedItem(tblPesanan.getValueAt(baris, 4).toString());
        warnaTxt.setText(tblPesanan.getValueAt(baris, 5).toString());
        sablonTxt.setSelectedItem(tblPesanan.getValueAt(baris, 6).toString());
        jumlahTxt.setValue(Integer.parseInt(tblPesanan.getValueAt(baris, 7).toString()));
        sizeTxt.setSelectedItem(tblPesanan.getValueAt(baris, 8).toString());
        genderTxt.setSelectedItem(tblPesanan.getValueAt(baris, 9).toString());
        hargaTxt.setValue(Integer.parseInt(tblPesanan.getValueAt(baris, 10).toString()));
        jTabbedPane1.setSelectedIndex(0);
    }
    
