    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        try {
            String sql = "delete from pemesanan where id_pesanan = '"+kodeTxt.getText()+"'";
            Connection conn=(Connection)Koneksi.bukaKoneksi();
            PreparedStatement pst=conn.prepareStatement(sql);
            Object[] tombol = {"Hapus","Batal"};
            int yatidak = JOptionPane.showOptionDialog(null, "Anda yakin ingin menghapus data ini?", "Konfirmasi hapus",
                    0, 2, null, tombol, tombol[1]);
            if(yatidak == JOptionPane.NO_OPTION){
                this.setDefaultCloseOperation(DO_NOTHING_ON_CLOSE);
            } else if(yatidak == JOptionPane.YES_OPTION){
                pst.execute();
                load_table_pesanan();
                EmptyForm();
                jTabbedPane1.setSelectedIndex(1);
            }
            
        } catch(HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal menghapus data:\n"+e.getMessage(), "Error", 0);
        }
    }//GEN-LAST:event_jButton6ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        try {
            Connection con = Koneksi.bukaKoneksi();
            Statement st = con.createStatement();
            //tempat data ditambahkan
            String sql = "update pemesanan set"
            + " nama_pemesan = '" + namaTxt.getText() + "'"
            + ", no_hp = '" + telpTxt.getText() + "'"
            + ", alamat = '" + alamatTxt.getText() + "'"
            + ", jenis_kain = '" + kainTxt.getSelectedItem().toString() + "'"
            + ", warna = '" + warnaTxt.getText() + "'"
            + ", jenis_sablon = '" + sablonTxt.getSelectedItem().toString() + "'"
            + ", jumlah = '" + jumlahTxt.getValue().toString() + "'"
            + ", size = '" + sizeTxt.getSelectedItem().toString() + "'"
            + ", jenis_kelamin = '" + genderTxt.getSelectedItem().toString() + "'"
            + ", harga = '" + hargaTxt.getValue().toString() + "'"
            + " where id_pesanan = '" + kodeTxt.getText() + "'";

            int row = st.executeUpdate(sql);

            //pesan jika data sukses ditambahkan
            if (row == 1) {
                JOptionPane.showMessageDialog(null, "Data berhasil diubah", "informasi", JOptionPane.INFORMATION_MESSAGE);
                load_table_pesanan();
                jTabbedPane1.setSelectedIndex(1);
                EmptyForm();
                con.close();
            }
        } //data gagal dimasukkan pada database
        catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data gagal diubah\n" + e.getMessage(), "informasi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

